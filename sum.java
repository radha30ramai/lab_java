// write a java program that accepts two integers and then print sum, diiffrence,  product and average of those numbers.

class sum
{
	public static void main(String args[])
	{
		int num1,num2,sum,diff,avg,product;
		num1=10;
		num2=20;
		sum=num1+num2;//to find sum
		diff=num1-num2;//to find diiffrence
		avg=sum/2;//to find average
		product=num1*num2;//to find product
		System.out.println("sum of="+sum);
		System.out.println("sum of="+diff);
		System.out.println("sum of="+avg);
		System.out.println("sum of="+product);
		
	}
}
	